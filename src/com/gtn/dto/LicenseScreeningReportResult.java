package com.gtn.dto;

public class LicenseScreeningReportResult {

	private String reportNo;
	private String reportType;
	private String reportDue;
	private String requiredInfo;
	private String earSection;
	private String status;
	
	
	public String getReportNo() {
		return reportNo;
	}
	public void setReportNo(String reportNo) {
		this.reportNo = reportNo;
	}
	public String getReportType() {
		return reportType;
	}
	public void setReportType(String reportType) {
		this.reportType = reportType;
	}
	public String getReportDue() {
		return reportDue;
	}
	public void setReportDue(String reportDue) {
		this.reportDue = reportDue;
	}
	public String getRequiredInfo() {
		return requiredInfo;
	}
	public void setRequiredInfo(String requiredInfo) {
		this.requiredInfo = requiredInfo;
	}
	public String getEarSection() {
		return earSection;
	}
	public void setEarSection(String earSection) {
		this.earSection = earSection;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
